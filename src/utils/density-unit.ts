/** Convert `kg/m³` to `kg/cm³`. */
export function kgm3ToKgcm3(kgm3: number) {
  return kgm3 * 1e-6
}

/** Convert `kg/m³` to `kg/mm³`. */
export function kgm3ToKgmm3(kgm3: number) {
  return kgm3 * 1e-9
}

/** Convert `kg/m³` to `kg/l`. */
export function kgm3ToKgL(kgm3: number) {
  return kgm3 * 1000
}
